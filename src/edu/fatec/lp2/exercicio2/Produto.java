package edu.fatec.lp2.exercicio2;

public class Produto {
	public String nome;
	public String unidade;
	public String descricao;
	public double preco;
	public double descontoMaximo;
	public Supermercado nomeSupermercado;
	
	
	
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getUnidade() {
		return unidade;
	}
	public void setUnidade(String unidade) {
		this.unidade = unidade;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public double getPreco() {
		return preco;
	}
	public void setPreco(double preco) {
		this.preco = preco;
	}
	public double getDescontoMaximo() {
		return descontoMaximo;
	}
	public void setDescontoMaximo(double descontoMaximo) {
		this.descontoMaximo = descontoMaximo;
	}
	public Supermercado getNomeSupermercado() {
		return nomeSupermercado;
	}
	public void setNomeSupermercado(Supermercado nomeSupermercado) {
		this.nomeSupermercado = nomeSupermercado;
	}
	
	
	

}
